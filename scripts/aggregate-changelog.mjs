/**
 * Aggregates backend/frontend/shared CHANGELOG.md files into a single
 * root CHANGELOG.md — the source of truth shown at /versioninfo.
 *
 * Runs after `changeset version` (wired in package.json → changeset:version).
 *
 * Why:
 *   Changesets writes one CHANGELOG per workspace package. In the Fixed-Group
 *   setup (ADR via docs/how-to/HOW-TO-USE-CHANGESETS.md) all three packages
 *   bump in lockstep, but `@assixx/shared` almost never receives direct
 *   changesets — so shared/CHANGELOG.md renders as empty version headers and
 *   `Updated dependencies [hash]` noise. Pointing /versioninfo at one of the
 *   per-package CHANGELOGs would mis-represent the release; pointing at shared
 *   (the previous behaviour) was the worst of the three.
 *
 *   This script merges the three files, drops auto-generated dependency
 *   noise, deduplicates bullets that repeat across packages, and writes a
 *   single human-readable root CHANGELOG.md.
 *
 * Output contract:
 *   - Re-generated on every `changeset:version` run. Do NOT hand-edit.
 *   - Merge conflicts on this file are resolved by re-running the script,
 *     not by manual merging.
 *
 * @see docs/how-to/HOW-TO-USE-CHANGESETS.md
 * @see frontend/src/routes/(app)/versioninfo/+page.server.ts — consumer
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { stdout } from 'node:process';

const SOURCES = ['backend/CHANGELOG.md', 'frontend/CHANGELOG.md', 'shared/CHANGELOG.md'];
const OUTPUT = 'CHANGELOG.md';
const SECTION_ORDER = ['Major Changes', 'Minor Changes', 'Patch Changes'];

const HEADER = `# Assixx

> All notable changes across backend, frontend, and shared packages.
> Auto-generated by \`scripts/aggregate-changelog.mjs\` from per-package
> CHANGELOGs after \`pnpm changeset:version\`. Do not hand-edit — changes
> will be overwritten on the next release.

`;

/**
 * Parse one CHANGELOG file into Map<version, Map<sectionName, Set<bullet>>>.
 * Preserves insertion order (relied upon for stable output).
 */
function parseChangelog(raw) {
  const versions = new Map();
  // Split on "## <digit…>" — skip first chunk (the top-level "# package" title).
  const blocks = raw.split(/^## (?=\d)/m).slice(1);
  for (const block of blocks) {
    const firstNewline = block.indexOf('\n');
    const version = block.slice(0, firstNewline).trim();
    const body = block.slice(firstNewline + 1);
    const sections = new Map();
    // Split body on "### <sectionName>".
    const sectionBlocks = body.split(/^### /m);
    for (const s of sectionBlocks) {
      const match = /^(Patch Changes|Minor Changes|Major Changes)\n([\s\S]*)/.exec(s);
      if (match === null) continue;
      const sectionName = match[1];
      const bullets = extractBullets(match[2]);
      if (!sections.has(sectionName)) sections.set(sectionName, new Set());
      for (const b of bullets) sections.get(sectionName).add(b);
    }
    // Always record the version, even if empty — prevents "missing" rows.
    versions.set(version, sections);
  }
  return versions;
}

/**
 * Group body lines into top-level bullets (each starting with "- " at col 0).
 * Continuation lines (indented or blank within a group) attach to the
 * current bullet. Bullets that turn into pure dependency noise after
 * normalization are dropped.
 */
function extractBullets(body) {
  const groups = [];
  let current = null;
  for (const line of body.split('\n')) {
    if (/^- /.test(line)) {
      if (current !== null) groups.push(current);
      current = line;
    } else if (current !== null && (line.startsWith(' ') || line.startsWith('\t'))) {
      current += '\n' + line;
    } else if (line.trim() === '' && current !== null) {
      groups.push(current);
      current = null;
    }
  }
  if (current !== null) groups.push(current);

  const out = [];
  for (const g of groups) {
    const normalized = normalize(g);
    if (normalized !== null) out.push(normalized);
  }
  return out;
}

/**
 * Drop changeset-generated cross-package noise, strip nested dep-update
 * lines from otherwise-valid bullets. Returns null if the whole bullet
 * should be dropped.
 */
function normalize(bullet) {
  const lines = bullet.split('\n');
  const firstLine = lines[0].trim();
  // Drop auto-generated top-level bullets entirely.
  if (/^- Updated dependencies(\s*\[[a-f0-9]+\])?$/.test(firstLine)) return null;
  if (/^- (@assixx\/shared|assixx-backend|assixx-frontend)@\d/.test(firstLine)) return null;
  // Strip nested "  - @assixx/shared@X.Y.Z" / "  - assixx-backend@X.Y.Z" lines.
  const kept = lines.filter(
    (l) => !/^\s+- (@assixx\/shared|assixx-backend|assixx-frontend)@\d/.test(l),
  );
  return kept.join('\n').trimEnd();
}

/**
 * Semver descending: "0.4.11" > "0.4.10" > "0.4.5" > "0.4.0".
 */
function compareVersions(a, b) {
  const pa = a.split('.').map(Number);
  const pb = b.split('.').map(Number);
  const len = Math.max(pa.length, pb.length);
  for (let i = 0; i < len; i++) {
    const da = pa[i] ?? 0;
    const db = pb[i] ?? 0;
    if (da !== db) return db - da;
  }
  return 0;
}

// --- Main ---
const parsed = SOURCES.map((p) => parseChangelog(readFileSync(p, 'utf8')));

const allVersions = new Set();
for (const p of parsed) for (const v of p.keys()) allVersions.add(v);
const sortedVersions = [...allVersions].sort(compareVersions);

let out = HEADER;
let emittedVersions = 0;
for (const version of sortedVersions) {
  const merged = new Map();
  for (const p of parsed) {
    const sections = p.get(version);
    if (sections === undefined) continue;
    for (const [name, bullets] of sections) {
      if (!merged.has(name)) merged.set(name, new Set());
      for (const b of bullets) merged.get(name).add(b);
    }
  }
  // Skip versions that have zero real content after dedup — keeps the
  // output lean (otherwise we'd emit ghost "## 0.4.11" blocks for shared).
  const hasContent = [...merged.values()].some((s) => s.size > 0);
  if (!hasContent) continue;

  out += `## ${version}\n\n`;
  for (const name of SECTION_ORDER) {
    const bullets = merged.get(name);
    if (bullets === undefined || bullets.size === 0) continue;
    out += `### ${name}\n\n`;
    for (const b of bullets) out += `${b}\n`;
    out += '\n';
  }
  emittedVersions += 1;
}

writeFileSync(OUTPUT, out);
stdout.write(
  `Root CHANGELOG.md aggregated: ${emittedVersions} versions from ${SOURCES.length} sources\n`,
);
